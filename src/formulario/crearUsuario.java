    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        comprobar();
        if (a>0){
        
        if(contra1.equalsIgnoreCase(contra2)){
           
            try{
                String ide=id.getText();
                String usua=usuario.getText();
                String sql="INSERT INTO usuario(idempleado,usuario,contrasena,estatus)VALUES('"+ide+"','"+usua+"',md5('"+contra2+"'),'Activo')";
                PreparedStatement pst=cn.prepareStatement(sql);
                
                int n=pst.executeUpdate();
                 
                if (n>0){
                  JOptionPane.showMessageDialog(null, "Usuario creado");
              
                        }
                 nombre.setText("");
            usuario.setText("");
            contra.setText("");
            confircontra.setText("");
            id.setText("");
            
                
                
            }catch (SQLException ex){
JOptionPane.showInputDialog(null, ex);
                
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Las contraseñas no son iguales");
            confircontra.setText("");
            contra.setText("");
            contra.requestFocus(true);
            
        }
        }
    }//GEN-LAST:event_jButton2ActionPerformed


    void comprobar(){
        if (id.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Escriba el digito");
            id.requestFocus(true);
            return;
        }
        if (nombre.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Escriba el nombre");
            nombre.requestFocus(true);
            return;
        }
        if (usuario.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Escriba el usuario");
            usuario.requestFocus(true);
            return;
        }
        contra1 =new String(contra.getPassword());
        if (contra1.equals("")){
            JOptionPane.showMessageDialog(null, "Escriba la contraseña");
            contra.requestFocus(true);
            return;
        }
        contra2 =new String(confircontra.getPassword());
        if (contra2.equals("")){
            JOptionPane.showMessageDialog(null, "Escriba la confirmacion de la contraseña");
            confircontra.requestFocus(true);
            return;
        }
        a++;
    }


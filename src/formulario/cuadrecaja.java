void cargarFacturas(){
        DefaultTableModel modelo= (DefaultTableModel)tablaefectivo.getModel();
        modelo.getDataVector().clear();
        
        String[] registros=new String[2];
        String sql="SELECT numerofactura,total FROM sesion WHERE idsesion='"+sesion.getText()+"'";
        
        
        try{
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            
            while (rs.next()){
                registros[0]=rs.getString("numerofactura");
                registros[1]=rs.getString("total");
                modelo.addRow(registros);
            }
            tablaefectivo.setModel(modelo);
            
        }catch (Exception ex){
            JOptionPane.showMessageDialog(null, ex);
            
        }
    }

void sumarTotal() {
        
        
        float total = 0f;
        String cuentas;
        
         for (int i = 0; i < tablaefectivo.getRowCount(); i++) {
            cuentas = (tablaefectivo.getValueAt(i, 1).toString());
            total=Float.parseFloat(cuentas);
            todo=todo+total;
            
         }
         
       dinerocogido.setText(String.valueOf(todo));
            
         
    }

void imprimir() {


        ArrayList lista3 = new ArrayList();

        for (int i = 0; i < tablaefectivo.getRowCount(); i++) {

            listaCuadre mortizar = new listaCuadre(tablaefectivo.getValueAt(i, 0) + "",tablaefectivo.getValueAt(i, 1) + "");
            lista3.add(mortizar);

        }
        

        JasperReport jr = null;

        try {
            jr = (JasperReport) JRLoader.loadObjectFromFile("cuadre.jasper");

            HashMap parametro = new HashMap();

            parametro.put("nombreempresa", "Empresa x");
            parametro.put("dineroinicial", dineroinicial.getText());
            parametro.put("dinerocogido", dinerocogido.getText());
            parametro.put("dinerototal", dinerototal.getText());
            
            JasperPrint jp = JasperFillManager.fillReport(jr, parametro, new JRBeanCollectionDataSource(lista3));

            JasperViewer jv = new JasperViewer(jp, false);
            jv.setVisible(true);

        } catch (JRException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error\n" + ex.getMessage());

        }

    }
